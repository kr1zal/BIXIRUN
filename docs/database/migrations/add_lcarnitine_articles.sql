1.  **Проблема:** Во время интенсивной работы гликолиз (расщепление глюкозы) происходит очень быстро, создавая избыток молекул ацетил-КоА. Этот избыток замедляет весь цикл выработки энергии и способствует накоплению лактата.
2.  **Решение:** L-карнитин связывает избыточный ацетил-КоА, образуя ацетил-L-карнитин. Это позволяет "разгрузить" метаболический конвейер, что приводит к двум ключевым эффектам:
    *   **Снижение накопления лактата:** Вы можете поддерживать высокую интенсивность дольше, отодвигая порог мышечного отказа.
    *   **Ускоренное восстановление:** Снижается мышечное повреждение (уровень креатинкиназы) и уменьшается посттренировочная боль (DOMS).

> **Вывод:** L-карнитин помогает не столько "сжечь жир" во время силовой тренировки, сколько повысить ее производительность и ускорить восстановление после нее, позволяя тренироваться чаще и эффективнее.

