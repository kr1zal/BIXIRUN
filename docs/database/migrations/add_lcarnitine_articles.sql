Его основная задача — **обеспечить транспорт жирных кислот в митохондрии** для их последующего преобразования в энергию (АТФ) через процесс β-окисления. Это не просто "сжигание жира", это фундаментальный механизм производства энергии, который позволяет поддерживать активность в течение дня.

